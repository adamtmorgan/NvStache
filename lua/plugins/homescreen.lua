				mapping_keys = true,
				cursor_column = 0.5,
				empty_lines_between_mappings = true,
				disable_statuslines = true,
				paddings = { height / 2 - 10, 2, 2, 0 },
			},
			mappings = {
				execute_command = "<CR>",
				open_section = "<TAB>",
				open_help = "?",
			},
			colors = {
				background = "default",
				-- background = "#1f2227",
				-- folded_section = "#56b6c2",
			},
			parts = { "header", "date_section", "body" },
